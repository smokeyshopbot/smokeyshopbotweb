def tr(lang: str | None, key: str, **kwargs) -> str:
    lang = normalize_lang(lang)
    text = TEXT.get(lang, TEXT[DEFAULT_LANGUAGE]).get(key)
    if text is None:
        text = TEXT[DEFAULT_LANGUAGE].get(key, key)
    try:
        return text.format(**kwargs)
    except Exception:
        return text
