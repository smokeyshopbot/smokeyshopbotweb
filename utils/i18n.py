from __future__ import annotations

SUPPORTED_LANGUAGES = ("en", "es")
LANGUAGE_NAMES = {
    "en": "English",
    "es": "Español",
}
DEFAULT_LANGUAGE = "en"


def normalize_lang(value: str | None) -> str:
    value = (value or "").strip().lower()
    if value in {"spanish", "espanol", "español"}:
        return "es"
    if value in {"english", "eng"}:
        return "en"
    return value if value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def lang_from_user(user: dict | None) -> str:
    return normalize_lang((user or {}).get("language") or (user or {}).get("language_code"))


def language_name(lang: str | None) -> str:
    return LANGUAGE_NAMES.get(normalize_lang(lang), LANGUAGE_NAMES[DEFAULT_LANGUAGE])


